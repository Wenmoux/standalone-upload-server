const CONVERT_CACHE_LIMIT = 800
const convertCache = new Map()

function getCacheKey(mode, input) {
  return `${mode}::${input}`
}

function getCachedConversion(key) {
  if (!convertCache.has(key)) return null
  const value = convertCache.get(key)
  convertCache.delete(key)
  convertCache.set(key, value)
  return value
}

function setCachedConversion(key, value) {
  convertCache.set(key, value)
  if (convertCache.size > CONVERT_CACHE_LIMIT) {
    const oldestKey = convertCache.keys().next().value
    convertCache.delete(oldestKey)
  }
  return value
}

function compilePhraseEntries(map) {
  return Object.keys(map)
    .sort((a, b) => b.length - a.length)
    .map(key => [key, map[key]])
}

function replaceChars(text, map) {
  return text.replace(/[\s\S]/g, ch => map[ch] || ch)
}

function replacePhrases(text, entries) {
  return entries.reduce((output, [from, to]) => output.split(from).join(to), text)
}

function protectPhrases(text, entries, rules) {
  const values = []
  let output = text
  entries.forEach(([from, to]) => {
    output = output.split(from).join(`\ue000${values.push(to) - 1}\ue001`)
  })
  rules.forEach(([pattern, to]) => {
    output = output.replace(pattern, () => `\ue000${values.push(to) - 1}\ue001`)
  })
  return { text: output, values }
}

function restoreProtected(text, values) {
  return text.replace(/\ue000(\d+)\ue001/g, (token, index) => values[Number(index)] || token)
}

export function convertText(text, mode) {
  const input = String(text || '')
  if (mode === 'simplified') {
    if (!input) return input

    const cacheKey = getCacheKey(mode, input)
    const cached = getCachedConversion(cacheKey)
    if (cached !== null) return cached

    const preConverted = replacePhrases(input, t2sPrePhraseEntries)
    const protectedResult = protectPhrases(preConverted, t2sProtectedPhraseEntries, t2sProtectedPhraseRules)
    const converted = replaceChars(openccToSimplified(protectedResult.text), t2sCharMap)
    return setCachedConversion(cacheKey, restoreProtected(converted, protectedResult.values))
  }

  if (mode === 'traditional') {
    const converted = replaceChars(openccToTraditional(replacePhrases(input, s2tPrePhraseEntries)), s2tCharMap)
    return replacePhrases(converted, s2tPostPhraseEntries)
  }

  return input
}

